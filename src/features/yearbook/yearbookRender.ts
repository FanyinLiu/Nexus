function pickLocale(uiLanguage: UiLanguage): LocaleStrings {
  return LOCALES[uiLanguage] ?? LOCALES['en-US']
}

function formatRange(startIso: string, endIso: string, dateLocale: string): string {
  const a = Date.parse(startIso)
  const b = Date.parse(endIso)
  if (!Number.isFinite(a) || !Number.isFinite(b)) return `${startIso} → ${endIso}`
  try {
    const fmt = new Intl.DateTimeFormat(dateLocale, { month: 'long', year: 'numeric' })
    return `${fmt.format(new Date(a))} — ${fmt.format(new Date(b))}`
  } catch {
    return `${startIso} → ${endIso}`
  }
}

function formatMonthLabel(monthStartIso: string, monthLocale: string): string {
  const t = Date.parse(monthStartIso)
  if (!Number.isFinite(t)) return monthStartIso
  try {
    return new Intl.DateTimeFormat(monthLocale, { month: 'long', year: 'numeric' }).format(new Date(t))
  } catch {
    return monthStartIso
  }
}

function formatLetterDate(letterDate: string, dateLocale: string): string {
  const t = Date.parse(letterDate)
  if (!Number.isFinite(t)) return letterDate
  try {
    return new Intl.DateTimeFormat(dateLocale, {
      month: 'long',
      day: 'numeric',
      year: 'numeric',
    }).format(new Date(t))
  } catch {
    return letterDate
  }
}

function formatSigned(x: number): string {
  if (x === 0) return '0.00'
  const sign = x > 0 ? '+' : ''
  return `${sign}${x.toFixed(2)}`
}

// ── Mini mood-map SVG ─────────────────────────────────────────────────────

const STRIP_W = 360
const STRIP_H = 60
const STRIP_PAD_X = 8
const STRIP_PAD_Y = 8

function renderMonthStrip(month: MonthlyBucket): string {
  if (month.bins.length < 2) {
    return `<svg viewBox="0 0 ${STRIP_W} ${STRIP_H}" class="strip-empty" role="img" aria-label="empty"></svg>`
  }
  const firstMs = Date.parse(month.bins[0].day)
  const lastMs = Date.parse(month.bins[month.bins.length - 1].day)
  const span = Math.max(lastMs - firstMs, 1)
  const innerW = STRIP_W - 2 * STRIP_PAD_X
  const innerH = STRIP_H - 2 * STRIP_PAD_Y

  const x = (day: string): number => {
    const t = Date.parse(day)
    const ratio = Number.isFinite(t) ? (t - firstMs) / span : 0
    return STRIP_PAD_X + ratio * innerW
  }
  const y = (valence: number): number => {
    const norm = (valence + 1) / 2
    return STRIP_PAD_Y + (1 - norm) * innerH
  }
  const neutral = STRIP_PAD_Y + 0.5 * innerH

  const path = month.bins
    .map((b, i) => `${i === 0 ? 'M' : 'L'}${x(b.day).toFixed(1)},${y(b.valence).toFixed(1)}`)
    .join(' ')

  return `<svg viewBox="0 0 ${STRIP_W} ${STRIP_H}" class="strip" role="img" aria-label="month">
    <line x1="${STRIP_PAD_X}" x2="${STRIP_W - STRIP_PAD_X}" y1="${neutral}" y2="${neutral}" stroke="rgba(0,0,0,0.1)" stroke-dasharray="2 3"/>
    <path d="${path}" stroke="#5a8a6a" stroke-width="1.4" fill="none"/>
  </svg>`
}

// ── Sections ──────────────────────────────────────────────────────────────

function renderYearOverview(snap: YearbookSnapshot, locale: LocaleStrings): string {
  const parts: string[] = []
  if (snap.yearSnapshot.baselineValence != null) {
    parts.push(`<dt>${escapeHtml(locale.baselineLabel)}</dt><dd>${escapeHtml(formatSigned(snap.yearSnapshot.baselineValence))}</dd>`)
  }
  if (snap.yearSnapshot.variability != null) {
    parts.push(`<dt>${escapeHtml(locale.variabilityLabel)}</dt><dd>${escapeHtml(snap.yearSnapshot.variability.toFixed(2))}</dd>`)
  }
  if (snap.yearSnapshot.inertia != null) {
    parts.push(`<dt>${escapeHtml(locale.inertiaLabel)}</dt><dd>${escapeHtml(snap.yearSnapshot.inertia.toFixed(2))}</dd>`)
  }
  parts.push(`<dt>${escapeHtml(locale.samplesLabel)}</dt><dd>${snap.yearSnapshot.n}</dd>`)

  return `<section class="overview">
    <h2>${escapeHtml(locale.yearOverviewHeader)}</h2>
    <dl>${parts.join('')}</dl>
  </section>`
}

function renderCoreg(snap: YearbookSnapshot, locale: LocaleStrings): string {
  const labelMap: Record<string, string> = {
    'co-regulating': locale.coregCoRegulating,
    'mirroring': locale.coregMirroring,
    'flat': locale.coregFlat,
    'unknown': locale.coregUnknown,
  }
  const label = labelMap[snap.coregKind]
  return `<section class="coreg">
    <h2>${escapeHtml(locale.coregHeader)}</h2>
    <p>${escapeHtml(label)}</p>
  </section>`
}

function renderMonthly(snap: YearbookSnapshot, locale: LocaleStrings): string {
  const rows = snap.months
    .map((m) => {
      const monthLabel = formatMonthLabel(m.monthStart, locale.monthLocale)
      const baselineText = m.snapshot.baselineValence != null
        ? formatSigned(m.snapshot.baselineValence)
        : `<span class="muted">${escapeHtml(locale.emptyMonthLabel)}</span>`
      return `<div class="month-row">
        <div class="month-label">${escapeHtml(monthLabel)}</div>
        <div class="month-strip">${renderMonthStrip(m)}</div>
        <div class="month-stat">${baselineText}</div>
      </div>`
    })
    .join('\n      ')
  return `<section class="monthly">
    <h2>${escapeHtml(locale.monthlyHeader)}</h2>
    <div class="month-list">
      ${rows}
    </div>
  </section>`
}

function renderLetterExcerpt(letter: SavedLetter, dateLocale: string): string {
  // First non-empty paragraph as the excerpt — typically the greeting.
  const candidates = [
    letter.content.greeting,
    letter.content.summary,
    letter.content.suggestion,
  ]
  const excerpt = candidates.find((s) => s && s.trim().length > 0) ?? ''
  return `<article class="letter-excerpt">
    <header>${escapeHtml(formatLetterDate(letter.letterDate, dateLocale))}</header>
    <p>${escapeHtml(excerpt.trim())}</p>
  </article>`
}

function renderLetters(snap: YearbookSnapshot, locale: LocaleStrings): string {
  if (snap.letters.length === 0) {
    return `<section class="letters">
      <h2>${escapeHtml(locale.lettersHeader)}</h2>
      <p class="muted">${escapeHtml(locale.lettersEmpty)}</p>
    </section>`
  }
  const items = snap.letters.map((l) => renderLetterExcerpt(l, locale.dateLocale)).join('\n      ')
  return `<section class="letters">
    <h2>${escapeHtml(locale.lettersHeader)}</h2>
    <div class="letter-list">
      ${items}
    </div>
  </section>`
}

function renderClosing(locale: LocaleStrings): string {
  return `<section class="closing-section">
    <h2>${escapeHtml(locale.closingHeader)}</h2>
    <p>${escapeHtml(locale.closingNote)}</p>
  </section>`
}

