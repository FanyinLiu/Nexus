export function buildYearbookFilename(uiLanguage: UiLanguage, now: Date = new Date()): string {
  const y = now.getFullYear()
  const m = String(now.getMonth() + 1).padStart(2, '0')
  const d = String(now.getDate()).padStart(2, '0')
  void uiLanguage
  return `nexus-yearbook-${y}-${m}-${d}.html`
}

export function renderYearbookHtml(
  snapshot: YearbookSnapshot,
  uiLanguage: UiLanguage,
): string {
  const locale = pickLocale(uiLanguage)
  const range = formatRange(snapshot.windowStart, snapshot.windowEnd, locale.dateLocale)

  return `<!DOCTYPE html>
<html lang="${escapeHtml(uiLanguage)}">
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <title>${escapeHtml(locale.pageHeader)}</title>
  <style>${STYLES}</style>
</head>
<body>
  <main>
    <header class="title-page">
      <div class="eyebrow">Nexus</div>
      <h1>${escapeHtml(locale.pageHeader)}</h1>
      <div class="range">${escapeHtml(range)}</div>
    </header>
    ${renderYearOverview(snapshot, locale)}
    ${renderCoreg(snapshot, locale)}
    ${renderMonthly(snapshot, locale)}
    ${renderLetters(snapshot, locale)}
    ${renderClosing(locale)}
  </main>
  <footer>${escapeHtml(locale.footerCredit)}</footer>
</body>
</html>
`
}
