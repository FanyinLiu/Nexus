/**
 * Generate the export filename for a letter. Stable, locale-aware,
 * filesystem-safe. Used by both the IPC save call and the browser
 * download fallback.
 */
export function buildLetterFilename(letter: SavedLetter): string {
  const date = letter.letterDate.replace(/[^0-9-]/g, '') || 'letter'
  return `nexus-letter-${date}.html`
}

/**
 * Render a SavedLetter as a self-contained HTML document.
 */
export function renderLetterHtml(letter: SavedLetter): string {
  const locale = pickLocale(letter.uiLanguage)
  const formattedDate = formatLetterDate(letter.letterDate, locale.dateLocale)

  const paragraphs: ReadonlyArray<{ text: string; closing?: boolean }> = [
    { text: letter.content.greeting },
    { text: letter.content.summary },
    { text: letter.content.suggestion },
    { text: letter.content.intention },
    { text: letter.content.experiment },
    { text: letter.content.closing, closing: true },
  ]

  const bodyHtml = paragraphs
    .filter((p) => p.text && p.text.trim().length > 0)
    .map((p) => {
      const escaped = escapeHtml(p.text.trim())
      const cls = p.closing ? 'closing' : ''
      return cls ? `<p class="${cls}">${escaped}</p>` : `<p>${escaped}</p>`
    })
    .join('\n      ')

  return `<!DOCTYPE html>
<html lang="${escapeHtml(letter.uiLanguage)}">
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <title>${escapeHtml(locale.pageHeader)} — ${escapeHtml(formattedDate)}</title>
  <style>${STYLES}</style>
</head>
<body>
  <main>
    <header>
      <div class="eyebrow">${escapeHtml(locale.pageHeader)}</div>
      <div class="date">${escapeHtml(formattedDate)}</div>
    </header>
    <div class="body">
      ${bodyHtml}
    </div>
  </main>
  <footer>${escapeHtml(locale.footerCredit)}</footer>
</body>
</html>
`
}
