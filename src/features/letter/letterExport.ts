/**
 * Letter export — renders a SavedLetter as a self-contained HTML document
 * the user can save locally, open in a browser, and (via the browser's
 * own print-to-PDF) keep as a permanent artifact.
 *
 * Design choices:
 *   - Pure render function. Takes a letter, returns HTML string. No IO.
 *   - Self-contained: all styling inlined. The file works offline, in
 *     any browser, with no external assets.
 *   - Typography that reads like a letter: serif body, generous line-
 *     height, narrow content column, calm header.
 *   - 5-locale safe — uses CSS font-family stacks that handle CJK + Latin
 *     gracefully without forcing webfont downloads.
 *   - The six paragraphs (greeting / summary / suggestion / intention /
 *     experiment / closing) render in fixed order with subtle dividers.
 *   - Output is intentionally light — the artifact-craft thesis is that
 *     the letter itself is the keepsake; ornamentation distracts.
 */

