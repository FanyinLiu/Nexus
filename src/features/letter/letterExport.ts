function pickLocale(uiLanguage: UiLanguage): LocaleStrings {
  return LOCALE_STRINGS[uiLanguage] ?? LOCALE_STRINGS['en-US']
}

/**
 * HTML-escape user-controlled strings so a stray `<script>` in a letter
 * body can't render as markup. Conservative pass — the SavedLetter
 * content is already produced by our prompt builder, but defence in
 * depth is cheap.
 */
export function escapeHtml(s: string): string {
  return s
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;')
}

function formatLetterDate(letterDate: string, dateLocale: string): string {
  const t = Date.parse(letterDate)
  if (!Number.isFinite(t)) return letterDate
  try {
    return new Intl.DateTimeFormat(dateLocale, {
      weekday: 'long',
      month: 'long',
      day: 'numeric',
      year: 'numeric',
    }).format(new Date(t))
  } catch {
    return letterDate
  }
}

